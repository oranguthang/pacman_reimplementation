function decode(rle, rowTiles, maxRows) {
  const rows = [];
  let row = [];
  let i = 0;
  const maxTiles = rowTiles * maxRows;
  let emitted = 0;
  while (i < rle.length) {
    const b = rle[i++];
    const run = (b >> 6) + 1;
    const tile = b & 0x3F;
    for (let r = 0; r < run; r++) {
      if (emitted >= maxTiles) {
        return rows;
      }
      row.push(tile);
      emitted++;
      if (row.length === rowTiles) {
        rows.push(row);
        row = [];
      }
    }
  }
  return rows;
}

const MAZE_ROWS = 27;
const MAZE_COLS = 22;

const maze = decode(rle, MAZE_COLS, MAZE_ROWS);

// Tile visual: wall vs empty
// Known from disassembly: $10-$1F = maze wall tiles, $2D = empty, $11 = dot, $03 = dot
// $00 = ghost house, $20 = spacer
const tileChar = (t) => {
  if (t === 0x2D) return ' '; // blank/space (border)
  if (t === 0x00) return ' '; // ghost house interior
  if (t === 0x20) return ' '; // space
  if (t === 0x11) return '.'; // pellet
  if (t === 0x01) return 'O'; // power pellet?
  if (t >= 0x10 && t <= 0x1F) return '#'; // wall tiles
  if (t >= 0x80 && t <= 0xBF) return '#'; // also wall (high runs)
  if (t === 0x03) return '.'; // pellet variant
  return '?';
};

